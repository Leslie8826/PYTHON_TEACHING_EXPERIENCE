# This program creates 3 tuples. It proceeds and 
# displays a few operations about the 3 tuples
tup1 = ('physics', 'chemistry', 2015, 2018)
tup2 = (1, 2, 3, 4, 5)
tup3 = "a", "b", "c", "d"

print("tup1[0] = ", tup1[0]) # display the tuple element at index 0
print("tup2[1:3] = ", tup2[1:3]) # display the element from index 1 to index 2
print("tup3 = ", tup3) # display the entire tuple tup3


##################
##### Output #####
##################
tup1[0] =  physics
tup2[1:3] =  (2, 3)
tup3 =  ('a', 'b', 'c', 'd')

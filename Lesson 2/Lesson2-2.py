
#The program has to ask a user to type 2 numbers
#Then the program will display the results of
# an addition, a substraction, a multiplication and a division
#between the 2 numbers

x = int(input("Enter the first number: "))
y = int(input("Enter the second number: "))
print("\n")

print("Arithmetic operations: ")

print ("Addition: ")
print(x, "+", y, "=", x+y)

print("Subtraction: ")
print(x, "-", y, "=", x-y)

print("Multiplication: ")
print(x, "*", y, "=", x*y)

print("Division: ")
print(x, "/", y, "=", x/y)



